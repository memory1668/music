package vo.util;

public class TempSignUpHaveStuInfo {
    private int stuId;
    private String stuName;
    private String stuIdNum;
    private String signUpPutTime;





    public int getStuId() {
        return stuId;
    }

    public void setStuId(int stuId) {
        this.stuId = stuId;
    }

    public String getStuName() {
        return stuName;
    }

    public void setStuName(String stuName) {
        this.stuName = stuName;
    }

    public String getStuIdNum() {
        return stuIdNum;
    }

    public void setStuIdNum(String stuIdNum) {
        this.stuIdNum = stuIdNum;
    }

    public String getSignUpPutTime() {
        return signUpPutTime;
    }

    public void setSignUpPutTime(String signUpPutTime) {
        this.signUpPutTime = signUpPutTime;
    }
}
